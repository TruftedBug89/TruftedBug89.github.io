// ── Helper: load existing topic data via vm ──────────────────────────
function loadTopic(filePath) {
  const raw = fs.readFileSync(filePath, 'utf8');
  // Remove the window assignment and IIFE so we can eval cleanly
  const clean = raw
    .replace(/window\.\w+ = \w+;\s*/, '')
    .replace(/\(function\(\)[\s\S]*?\}\)\(\);\s*$/, '');

  const sandbox = {
    Utils: { randomItems: (arr, n) => arr.slice(0, n) },
    module: { exports: null }
  };
  vm.runInNewContext(clean + '\nmodule.exports = ' + raw.match(/const (\w+) = \{/)[1] + ';', sandbox);
  return { data: sandbox.module.exports, raw, varName: raw.match(/const (\w+) = \{/)[1] };
}

// ── Example generation ──────────────────────────────────────────────
function makeExamples(ch, py, en) {
  const v_en = en.toLowerCase();
  const patterns = [
    { cn: `我${ch}。`, py: `wǒ ${py}`, en: `I ${v_en}.` },
    { cn: `我们一起${ch}。`, py: `wǒ men yì qǐ ${py}`, en: `Let's ${v_en} together.` },
    { cn: `他${ch}。`, py: `tā ${py}`, en: `He/she ${v_en}.` },
    { cn: `你${ch}吗？`, py: `nǐ ${py} ma？`, en: `Do you ${v_en}?` },
    { cn: `这个${ch}很好。`, py: `zhè ge ${py} hěn hǎo`, en: `This ${v_en} is good.` },
    { cn: `我喜欢${ch}。`, py: `wǒ xǐ huan ${py}`, en: `I like ${v_en}.` },
    { cn: `今天${ch}。`, py: `jīn tiān ${py}`, en: `Today ${v_en}.` },
    { cn: `我们要${ch}。`, py: `wǒ men yào ${py}`, en: `We want to ${v_en}.` },
    { cn: `${ch}很重要。`, py: `${py} hěn zhòng yào`, en: `${v_en} is very important.` },
    { cn: `去${ch}。`, py: `qù ${py}`, en: `Go ${v_en}.` },
  ];
  const h1 = hashStr(ch + py);
  const h2 = hashStr(en);
  const idx1 = h1 % patterns.length;
  let idx2 = (idx1 + 3 + h2 % 5) % patterns.length;
  if (idx2 === idx1) idx2 = (idx2 + 1) % patterns.length;
  return [patterns[idx1], patterns[idx2]];
}

function hashStr(s) {
  let hash = 0;
  for (let i = 0; i < s.length; i++) {
    hash = ((hash << 5) - hash) + s.charCodeAt(i);
    hash |= 0;
  }
  return Math.abs(hash);
}

function esc(v) {
  return String(v).replace(/'/g, "\\'");
}

