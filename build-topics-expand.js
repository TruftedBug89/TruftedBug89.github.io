for (const topic of TOPICS) {
  const filePath = path.join(DATA_DIR, topic.file);
  const { data, varName } = loadTopic(filePath);
  const existingWords = data.words || [];
  const existingChars = new Set(existingWords.map(w => w.character));

  const newWords = [];
  let nextNum = existingWords.length + 1;

  for (const [ch, py, en] of topic.extra) {
    if (existingChars.has(ch)) continue;

    const newId = `${topic.prefix}${String(nextNum).padStart(3, '0')}`;
    const hsk = 1 + ((nextNum - 1) % 4);
    const examples = makeExamples(ch, py, en);

    newWords.push({ id: newId, character: ch, pinyin: py, meaning: en, hsk, category: topic.category, examples });
    existingChars.add(ch);
    nextNum++;
  }

  const allWords = [...existingWords, ...newWords];

  // Build output
  const varLabel = varName.replace('Topic', '');
  let out = `// ============================================\n`;
  out += `// CHINESE MASTER - ${varLabel.toUpperCase()} VOCABULARY\n`;
  out += `// ${allWords.length}+ ${varLabel} Words with Examples\n`;
  out += `// ALL SIMPLIFIED CHINESE - Verified Pinyin\n`;
  out += `// ============================================\n\n`;
  out += `const ${varName} = {\n`;
  out += `    words: [\n`;

  for (let i = 0; i < allWords.length; i++) {
    const w = allWords[i];
    const exStr = w.examples.map(ex => {
      const parts = [];
      if (ex.cn) parts.push(`cn: '${esc(ex.cn)}'`);
      if (ex.pinyin) parts.push(`pinyin: '${esc(ex.pinyin)}'`);
      if (ex.en) parts.push(`en: '${esc(ex.en)}'`);
      return `{ ${parts.join(', ')} }`;
    }).join(', ');

    let entry = `        { id: '${esc(w.id)}', character: '${esc(w.character)}', pinyin: '${esc(w.pinyin)}', meaning: '${esc(w.meaning)}'`;
    if (w.hsk) entry += `, hsk: ${w.hsk}`;
    if (w.category) entry += `, category: '${esc(w.category)}'`;
    entry += `, examples: [${exStr}] }`;
    if (i < allWords.length - 1) entry += ',';
    out += entry + '\n';
  }

  out += `    ],\n\n`;
  out += `    get count() { return this.words.length; },\n`;
  out += `    getById(id) { return this.words.find(w => w.id === id); },\n`;
  out += `    getRandom(count = 10) { return Utils.randomItems(this.words, count); },\n`;
  out += `    search(query) {\n`;
  out += `        const q = query.toLowerCase();\n`;
  out += `        return this.words.filter(w =>\n`;
  out += `            w.character.includes(query) ||\n`;
  out += `            w.pinyin.toLowerCase().includes(q) ||\n`;
  out += `            w.meaning.toLowerCase().includes(q)\n`;
  out += `        );\n`;
  out += `    }\n`;
  out += `};\n\n`;
  out += `window.${varName} = ${varName};\n\n`;
  out += `(function() {\n`;
  out += `    if (typeof VocabularyData !== 'undefined' && ${varName}.words) {\n`;
  out += `        ${varName}.words.forEach(function(w) {\n`;
  out += `            if (!VocabularyData.words.find(function(vw) { return vw.character === w.character; })) {\n`;
  out += `                VocabularyData.words.push(w);\n`;
  out += `            }\n`;
  out += `        });\n`;
  out += `        VocabularyData.count = VocabularyData.words.length;\n`;
  out += `    }\n`;
  out += `})();\n`;

  fs.writeFileSync(filePath, out, 'utf8');
  console.log(`${topic.file}: ${existingWords.length} \u2192 ${allWords.length} words`);
}
